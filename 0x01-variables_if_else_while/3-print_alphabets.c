#include <stdio.h>

/**
 * main - Entry point
 * main - prints the alphabet in lowercase, and then in uppercase,
 * followed by a new line
 * Return: 0 Is a success
*/

int main(void)
{
	char a;

	char b;

	a = 'a';
	b = 'A';

	while
	(a <= 'z') {
		putchar(a);
		a++;
	}

	while
	(b <= 'Z') {
		putchar(b);
		b++;
	}
return (0);
}
